package com.infinite.hibernate.JoinusingCriteria;

public class Uni_Student {
	
	private int student_Id;
	private String student_Name;
	private String student_Address;
	private int student_Ph;
	
	/**
	 * @return the student_Id
	 */
	public int getStudent_Id() {
		return student_Id;
	}
	/**
	 * @param student_Id the student_Id to set
	 */
	public void setStudent_Id(int student_Id) {
		this.student_Id = student_Id;
	}
	/**
	 * @return the student_Name
	 */
	public String getStudent_Name() {
		return student_Name;
	}
	/**
	 * @param student_Name the student_Name to set
	 */
	public void setStudent_Name(String student_Name) {
		this.student_Name = student_Name;
	}
	/**
	 * @return the student_Address
	 */
	public String getStudent_Address() {
		return student_Address;
	}
	/**
	 * @param student_Address the student_Address to set
	 */
	public void setStudent_Address(String student_Address) {
		this.student_Address = student_Address;
	}
	/**
	 * @return the student_Ph
	 */
	public int getStudent_Ph() {
		return student_Ph;
	}
	/**
	 * @param student_Ph the student_Ph to set
	 */
	public void setStudent_Ph(int student_Ph) {
		this.student_Ph = student_Ph;
	}

}
