package com.infinite.hibernate.thirdTable;

public class Customer {
	private int customer_Id;
	private String customer_Name;
	private int customer_AccNo;
	private int customer_Ph;


	/**
	 * @return the customer_Id
	 */
	public int getCustomer_Id() {
		return customer_Id;
	}
	/**
	 * @param customer_Id the customer_Id to set
	 */
	public void setCustomer_Id(int customer_Id) {
		this.customer_Id = customer_Id;
	}
	/**
	 * @return the customer_Name
	 */
	public String getCustomer_Name() {
		return customer_Name;
	}
	/**
	 * @param customer_Name the customer_Name to set
	 */
	public void setCustomer_Name(String customer_Name) {
		this.customer_Name = customer_Name;
	}
	/**
	 * @return the customer_AccNo
	 */
	public int getCustomer_AccNo() {
		return customer_AccNo;
	}
	/**
	 * @param customer_AccNo the customer_AccNo to set
	 */
	public void setCustomer_AccNo(int customer_AccNo) {
		this.customer_AccNo = customer_AccNo;
	}
	/**
	 * @return the customer_Ph
	 */
	public int getCustomer_Ph() {
		return customer_Ph;
	}
	/**
	 * @param customer_Ph the customer_Ph to set
	 */
	public void setCustomer_Ph(int customer_Ph) {
		this.customer_Ph = customer_Ph;
	}

}
